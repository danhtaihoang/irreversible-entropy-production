
   PROGRAM chi_calcul
   IMPLICIT NONE
     
   INTEGER (KIND=8),PARAMETER::n=50
 
   INTEGER (KIND=8) :: i
   
   REAL (KIND=8),DIMENSION(n) :: eps,E,Cv,M1,M2,M3,chi1,chi2,chi3

!!!=======================================================================================
!!!======================================================================================= 
!!!=======================================================================================
!!!=======================================================================================  
   OPEN(unit=11,file='E_av_200_1.dat')  
   OPEN(unit=12,file='E_av_200_1.txt') 
 
   DO i=1,n
      READ(11,*)eps(i),E(i),Cv(i),M1(i),M2(i),M3(i),chi1(i),chi2(i),chi3(i)
   END DO  
   
   DO i=1,n
      IF (eps(i)/=0.) THEN
      WRITE(12,*)abs(eps(i)),E(i),Cv(i),M1(i),M2(i),M3(i),chi1(i),chi2(i),chi3(i)
      END IF
   END DO 
  
!!!=======================================================================================
      
   CLOSE(11)
   CLOSE(12)  
    
   END PROGRAM chi_calcul

